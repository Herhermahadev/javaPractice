package operators;

public class ArithmeticOperators {
    //  + - * % /


    public static void main(String[] args) {
        int chair=20;
        int people=19;


        System.out.println(chair+people);
        System.out.println("Total of chair and people is: "+(chair+people));
        System.out.println("Difference of people and chair is: "+(chair-people));
        System.out.println("Multiplication of both is: "+(chair*people));
        System.out.println("Division of both is: "+(chair/people));
        System.out.println("Mod division of both: "+(chair%people));
        




    }
}
